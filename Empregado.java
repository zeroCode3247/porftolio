public class Empregado{
    private String nome;
    private int idade;

    public String getNome(){
        return this.nome;
    }

    public void setNome(String nome){
        this.nome = nome;
    }

    public int getIdade(){
        return this.idade;
    }

    public void setNome(int idade){
        this.idade = idade;
    }
}